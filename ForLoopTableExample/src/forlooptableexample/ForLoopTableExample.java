package forlooptableexample;

/**
 * Nawaal Fatima 21st September,2017 ForLoopTableExample to print the column
 * format of numbers(1-6) and their squares
 *
 * @author fatin9973
 */
public class ForLoopTableExample {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        for (int i = 1; i <= 6; i++) {


            System.out.println(i + "           " + i * i);


        }
    }
}
