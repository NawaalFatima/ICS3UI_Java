public class OutputExamples {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println(2+2);
        System.out.println("2+2");
        System.out.println(7*6);
        System.out.println(24/6);
        System.out.println(25/6);   //integer division - no decimals in answer
        System.out.println(25.0/6); // add a decimal to get a decimal answer
        System.out.println(25/6.0); // same as above
        System.out.println(25%6);  // gives the remainder when #s are divided
        // this is called the modulus operator
        System.out.println("nawaal"+3); // to put strings together use a + sign
        //this is called as CONCATENATION-putting things together to print a statement
        System.out.print("Hello");
        System.out.println("World");
        
        
    }
}
